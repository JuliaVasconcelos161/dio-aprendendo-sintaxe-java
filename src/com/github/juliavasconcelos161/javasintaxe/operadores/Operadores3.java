package com.github.juliavasconcelos161.javasintaxe.operadores;

public class Operadores3 {
    public static void main(String[] args) {
        int numero = 5;

        //x repeticao
        numero = numero + 1;
        numero++;
        System.out.println(++numero);
        System.out.println(numero++);
        System.out.println(numero);
    }
}
